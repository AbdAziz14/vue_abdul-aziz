const luasPersegi = (sisi1, sisi2) => {
    return sisi1 * sisi2
}

const luasLingkaran = (jari1, jari2) => {
    return 3.14 * (jari1 * jari2)
}

const luasKubus = (sisi1, sisi2, sisi3) => {
    return sisi1 * sisi2 * sisi3;
}

const volumeKubus = (sisi1, sisi2) => {
    return sisi1 * sisi2
}
  
export { luasPersegi,luasLingkaran,luasKubus,volumeKubus }
  